    {
        $todate = request('todate');
        $fromdate = request('fromdate');
        $cpatients = Curing_Patient::select("curing__patients.*")
            ->whereBetween('start_date', [$fromdate,$todate])
            ->get();
    
        return view('patients.cpresult',compact('cpatients'));
    }

